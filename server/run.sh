#!/bin/bash -x
PRG="$0"

while [ -h "$PRG" ]; do
  ls=`ls -ld "$PRG"`
  link=`expr "$ls" : '.*-> \(.*\)$'`
  if expr "$link" : '/.*' > /dev/null; then
    PRG="$link"
  else
    PRG=`dirname "$PRG"`/"$link"
  fi
done

PRGDIR=`dirname "$PRG"`

./$PRGDIR/target/finangular-0.0.1-SNAPSHOT-dist/finangular-0.0.1-SNAPSHOT/bin/run.sh